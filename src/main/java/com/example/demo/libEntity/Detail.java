package com.example.demo.libEntity;

public class Detail {
    private String bookName;
    private String gcfb;
    private String kjfb;
    private String author;
    private String cbs;

    public String getBookName() {
        return bookName;
    }

    public void setBookName(String bookName) {
        this.bookName = bookName;
    }

    public String getGcfb() {
        return gcfb;
    }

    public void setGcfb(String gcfb) {
        this.gcfb = gcfb;
    }

    public String getKjfb() {
        return kjfb;
    }

    public void setKjfb(String kjfb) {
        this.kjfb = kjfb;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getCbs() {
        return cbs;
    }

    public void setCbs(String cbs) {
        this.cbs = cbs;
    }
}
